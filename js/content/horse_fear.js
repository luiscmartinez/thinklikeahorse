    <p align="justify">
      I called these people "Fakers". They are phony, scared, uneducated about
      horses and think they are being so smart. Fakers think they are fooling
      the horse and fooling others. They are easy to spot if you know what to
      look for. They will be loud, they will be talking and explaining what they
      do and why, they will be trying to justify how smart they are and how much
      they know. They will always want to share their limited and mostly wrong
      knowledge by always giving and offering unwanted advice. They will justify
      things like my horse needs spurs, my horse needs a bit, my horse needs
      tough love, all of this is just a cover for their fear and inability to
      progress and learn better ways. However, the Fakers think they are fooling
      others and fooling their horse. Those who know are not fooled and the
      horse is never fooled. The horse may go along, it may not exploit their
      ignorance and it may just accept them in certain circumstances. But the
      horse will never develop a bond or trust, it will not grow with the Faker
      since the Faker is in denial, the Faker has convinced themselves that they
      are not the problem and it is the horse's fault and the horse is the one
      with all the problems.
    </p>
