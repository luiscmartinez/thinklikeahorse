<p align="justify">Another advantage to the rope halter is when a horse pulls, when wearing a rope halter tied correctly, the rope pulls behind the ears, puts pressure on the pole and is not comfortable. This means the horse will learn to give to give to pressure, pull less and will learn it cannot get free, which are all good lessons. The opposite if true with a nice soft, wide, fur lined nylon halters. The rope halter is only as strong as the knot holding it. Tying the knot correctly, on either the lead rope or halter, is a very important point that is often overlooked. I see lots of rope halters used and very few are tied correctly. If not tied right, it will work loose and the horse will learn to pull and get free. A correct knot will not work loose and will properly secure the horse. An incorrect knot will allow the horse to get free. So when a horse gets free it does not matter if it got free from the snap breaking, the knot giving or the knot securing him to the tie point gives, the horse learns the same lesson, he can get free if he pulls, so you teach a horse to pull.</p>
